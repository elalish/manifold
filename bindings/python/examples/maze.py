from manifold3d import Manifold
import numpy as np

n = 10
rad = 0.4

def rod1(a, b, r, fn = 20):
    a = np.array(a)
    b = np.array(b)
    dir = b - a
    h = np.linalg.norm(dir)
    if dir[0] == 0 and dir[1] == 0:
        return Manifold.cylinder(h, r, -1, fn).translate(b if a[2] > b[2] else a)
    else:
        w = dir / h
        u0 = np.cross(w, np.array([0, 0, 1]))
        u = u0 / np.linalg.norm(u0)
        v0 = np.cross(w, u)
        v = v0 / np.linalg.norm(v0)
        return Manifold.cylinder(h, r, -1, fn).transform(np.dstack([u, v, w, a])[0])

def ball1(x, r, fn=20):
    return Manifold.sphere(r, fn).translate(x)

